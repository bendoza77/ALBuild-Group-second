const PolicyModal = ({ type, onClose }) => {
  const data = policies[type]

  useEffect(() => {
    document.body.style.overflow = 'hidden'
    const onKey = e => e.key === 'Escape' && onClose()
    window.addEventListener('keydown', onKey)
    return () => {
      document.body.style.overflow = ''
      window.removeEventListener('keydown', onKey)
    }
  }, [onClose])

  if (!data) return null

  return (
    <div
      onClick={onClose}
      className="fixed inset-0 z-[100] bg-black/60 backdrop-blur-sm flex items-center justify-center p-4"
    >
      <div
        onClick={e => e.stopPropagation()}
        className="bg-[#fff7ed] rounded-3xl shadow-2xl w-full max-w-2xl max-h-[85vh] flex flex-col"
      >
        {/* Header */}
        <div className="flex items-center justify-between px-8 py-6 border-b border-[#f5e6d3]">
          <h2 className="text-xl font-extrabold text-[#7c2d12]">{data.title}</h2>
          <button
            onClick={onClose}
            className="text-gray-400 hover:text-[#7c2d12] transition-colors p-1 rounded-lg"
            aria-label="დახურვა"
          >
            <svg className="w-6 h-6" fill="none" stroke="currentColor" viewBox="0 0 24 24">
              <path strokeLinecap="round" strokeLinejoin="round" strokeWidth={2} d="M6 18L18 6M6 6l12 12" />
            </svg>
          </button>
        </div>

        {/* Scrollable body */}
        <div className="overflow-y-auto px-8 py-6 space-y-6">
          {data.sections.map(s => (
            <div key={s.heading}>
              <h3 className="font-bold text-[#7c2d12] mb-2">{s.heading}</h3>
              <p className="text-gray-600 text-sm leading-relaxed">{s.body}</p>
            </div>
          ))}
          <p className="text-xs text-gray-400 pt-2">
            ბოლო განახლება: 1 იანვარი, 2025
          </p>
        </div>
      </div>
    </div>
  )
}

export default PolicyModal
